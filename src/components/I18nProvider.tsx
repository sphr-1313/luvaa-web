interface I18nContextType {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string) => string;
  availableLocales: { code: Locale; label: string }[];
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}

export default function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  useEffect(() => {
    const stored = typeof window !== "undefined" ? localStorage.getItem("luvaa_locale") : null;
    if (stored && ["en", "nl", "de", "fr", "tr"].includes(stored)) {
      setLocaleState(stored as Locale);
    }
  }, []);

  function setLocale(l: Locale) {
    setLocaleState(l);
    if (typeof window !== "undefined") {
      localStorage.setItem("luvaa_locale", l);
    }
  }

  const t = useMemo(() => {
    return (key: string) => {
      const parts = key.split(".");
      let result: any = translations[locale];
      for (const p of parts) {
        if (result && typeof result === "object" && p in result) {
          result = result[p];
        } else {
          return key;
        }
      }
      return typeof result === "string" ? result : key;
    };
  }, [locale]);

  const availableLocales = [
    { code: "en" as Locale, label: "English" },
    { code: "nl" as Locale, label: "Nederlands" },
    { code: "de" as Locale, label: "Deutsch" },
    { code: "fr" as Locale, label: "Français" },
    { code: "tr" as Locale, label: "Türkçe" },
  ];

  return (
    <I18nContext.Provider value={{ locale, setLocale, t, availableLocales }}>
      {children}
    </I18nContext.Provider>
  );
}
